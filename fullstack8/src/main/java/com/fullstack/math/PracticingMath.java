package com.fullstack.math;

public class PracticingMath {

	public static void main(String[] args) {

		System.out.println(Math.pow(2, 3)); //potency		
		System.out.println();		
		System.out.println(Math.round(4.7));// round up
		System.out.println();		
		System.out.println(Math.round(4.4));// round down
		System.out.println();		
		System.out.println(Math.floor(4.4));
		System.out.println();		
		System.out.println(Math.round(Math.random() * 100));
		System.out.println();		
		System.out.println(Math.sin(Math.toRadians(30)));
		System.out.println();		
		System.out.println(Math.cos(Math.toRadians(1)));
		System.out.println();		
		System.out.println(Math.tan(Math.toRadians(45)));
		
		

}
}